    public String palindrome(String datoEntrante){
        String datoSaliente=""; 
        for (int i = datoEntrante.length()-1; i >= 0 ; i--) {
            datoSaliente += datoEntrante.charAt(i);
        }if(datoSaliente.equals(datoEntrante )){
            return"Si es un palindromo!!!\nPalabra: "+datoEntrante;
        }
        return "No es un palindromo!!\nPalabra: "+datoEntrante;
    }
    
    
